function stableHash(input) {
  let hash = 2166136261;
  for (let i = 0; i < input.length; i += 1) {
    hash ^= input.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function randomizedMogScore(score, seedKey) {
  const clamped = Math.min(Math.max(score, 0), 10);
  const seed = stableHash(String(seedKey || "mogscan"));
  const variance = (seed % 100) / 100; // 0.00 to 0.99
  const randomized = clamped * 10 + variance;
  return Math.min(Math.max(randomized, 40), 99.9);
}

export function formatScore(_score, rankLabel, _seedKey = "") {
  const label = rankLabel || tier(_score);
  const emoji = tierEmoji(label);
  return `${emoji ? `${emoji} ` : ""}${label}`;
}
